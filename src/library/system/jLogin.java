    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        Connect conn = new Connect();
        String username = jTextFieldUsername.getText();
        String password = jTextFieldPassword.getText();
        if(jCheckBoxLoginAdmin.isSelected()){
            boolean check = conn.loginAdmin(username, password);
            if(check){
                jAdminMenu admin = new jAdminMenu ();
                admin.show();
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Incorrect Username/Password");
            }
        }else{
            Account acc = conn.login(username, password);
            if(acc != null){
                jMenu menu = new jMenu();     
                menu.user(acc);
                menu.show();
                this.dispose();            
            }else{
                JOptionPane.showMessageDialog(this, "Incorrect Username/Password");
            }
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed


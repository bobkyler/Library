    private void jCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCreateActionPerformed
        // TODO add your handling code here:
        id = jID.getText();
        firstname = jFirst.getText();
        middlename = jMiddle.getText();
        lastname = jLast.getText();
        telephone = jTel.getText();
        username = firstname.concat("."+id);
        password = lastname.concat("123");

        if(id.equals("") || firstname.equals("") || middlename.equals("") || lastname.equals("") || telephone.equals("")){
            JOptionPane.showMessageDialog(this, "Please recheck your information"); 
        }else{
            Account acc = new Account(id, firstname, middlename, lastname, telephone, username, password);
            boolean check = conn.addAccount(acc);

            if(check){
                JOptionPane.showMessageDialog(this,"You have successfully created an account");
                jLogin log = new jLogin();
                log.show();
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Please recheck your information");
            }
        }
    }//GEN-LAST:event_jCreateActionPerformed


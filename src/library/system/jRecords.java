    private void jDeleteReserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteReserveActionPerformed
        // TODO add your handling code here:
        String bookID = jTblReserved.getValueAt(index, 0).toString();
        String title = jTblReserved.getValueAt(index, 1).toString();
        String date = jTblReserved.getValueAt(index, 3).toString();
        boolean check = conn.deleteReservation(acc, bookID, date);
        if(check){
            JOptionPane.showMessageDialog(this, "You cancelled your reservation on "+title);
        }
        tblReserve.removeRow(index);
        jDeleteReserve.setEnabled(false);
    }//GEN-LAST:event_jDeleteReserveActionPerformed


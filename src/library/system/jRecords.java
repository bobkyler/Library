    public jRecords(Account user) {
        initComponents();
        
        jBack.setOpaque(false);
        jBack.setContentAreaFilled(false); 
        jBack.setBorderPainted(false);
        
        
        this.acc = user;
        jWelcome.setText("Welcome "+acc.getFirstname()+ "!");
        conn = new Connect();
        
        //for Borrow Table
        ResultSet rs = null;
        tblBorrow = (DefaultTableModel)jTblBorrow.getModel();
        rs = conn.borrowDB(acc);
        try{
            while(rs.next()){
               String data[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(6)};
               tblBorrow.addRow(data);
            }
        }catch(SQLException e){
            System.out.println(e.toString());
        }
        
        //for Return Table
        tblReturn = (DefaultTableModel)jTblReturned.getModel();
        rs = conn.returnDB(acc);
        try{
            while(rs.next()){
               String data[] = {rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(8)};
               tblReturn.addRow(data);
            }
        }catch(SQLException e){
            System.out.println(e.toString());
        }
        
        //for Reserve table
        tblReserve = (DefaultTableModel)jTblReserved.getModel();
        rs = conn.reserveDB(acc);
        try{
            while(rs.next()){
               String data[] = {rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(6)};
               tblReserve.addRow(data);
            }
        }catch(SQLException e){
            System.out.println(e.toString());
        }
        jDeleteReserve.setEnabled(false);
    }


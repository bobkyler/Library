    private void jReserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jReserveActionPerformed
        // TODO add your handling code here:
        String month = jMonth.getText();
        String day = jDay.getText();
        String year = jYear.getText();
        if(signal == true){
            JOptionPane.showMessageDialog(this, "You have currently borrowed this book. Please return it first and Try again later.");
            signal = false;
        }else{
            try{
                boolean check = false;
                if(currYear == Integer.parseInt(year)){ //checking if the day or month is lesser than the current date where they have they same year
                   if(Integer.parseInt(month) < currMonth || Integer.parseInt(day) < currDay){
                        check = true;  //there's an error in month and day
                    }
                }

                if(jDay.getText().equals("") || jMonth.getText().equals("")  || jYear.getText().equals("")
                        || Integer.parseInt(month)>12 || Integer.parseInt(day)>31 || Integer.parseInt(year) < currYear || check == true){

                    JOptionPane.showMessageDialog(this, "Incorrect Reservation Date");
                }else{
                    int numDay = jComboBoxNumDays.getSelectedIndex() + 1;       //+1 so that it will coincide with the day
                    int newDay = Integer.parseInt(day) + numDay;
                    day = Integer.toString(newDay);
                    String dueDate = month+"/"+day+"/"+year;
                    String reserveDate = month+"/"+jDay.getText()+"/"+year;

                    Book currBook = books.get(index);
                    conn.reserve(acc, currBook, reserveDate, dueDate);
                    JOptionPane.showMessageDialog(this, "You have reserved a book");

                }
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Please recheck your Date of Reservation");
            }
        }
        
    }//GEN-LAST:event_jReserveActionPerformed


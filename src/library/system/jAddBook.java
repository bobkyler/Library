    private void jBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddActionPerformed
        // TODO add your handling code here:
        String id = jBookID.getText();
        String title = jTitle.getText();
        String author = jAuthor.getText();
        
        if(id.equals("") || title.equals("") || author.equals("")){
            JOptionPane.showMessageDialog(this, "Please recheck your information"); 
        }else{
            boolean check = conn.addBook(id, title, author);
            if(check){
                JOptionPane.showMessageDialog(this,"You have successfully added a book");
                String data[] = {id, title, author};
                tbl.addRow(data);
            }else{
                JOptionPane.showMessageDialog(this,"Please recheck your information");
            }
        }
    }//GEN-LAST:event_jBtnAddActionPerformed

    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        // TODO add your handling code here:
        
        String id = jBookID.getText();
        String title = jTitle.getText();
        String author = jAuthor.getText();
        boolean check = conn.updateBook(id, title, author);
      
        if(check == true){
            JOptionPane.showMessageDialog(this,"Updated Successfully");
            tbl.setValueAt(title, index, 1);
            tbl.setValueAt(author, index, 2);
        }
    }//GEN-LAST:event_jBtnUpdateActionPerformed

    private void jTblBookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblBookMouseClicked
        // TODO add your handling code here:
        
        index = jTblBook.getSelectedRow();
        jBookID.setText(tbl.getValueAt(index,0).toString());
        jTitle.setText(tbl.getValueAt(index,1).toString());
        jAuthor.setText(tbl.getValueAt(index,2).toString());
    }//GEN-LAST:event_jTblBookMouseClicked

    private void jBtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDeleteActionPerformed
        // TODO add your handling code here:
        
        String bookid = tbl.getValueAt(index,0).toString();
        boolean check = conn.deleteBook(bookid);
        if(check == true){
            JOptionPane.showMessageDialog(this,"You have successfully deleted an account");
            tbl.removeRow(index);
        } 
    }//GEN-LAST:event_jBtnDeleteActionPerformed


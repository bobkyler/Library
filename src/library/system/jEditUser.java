    private void jButtonCreateAccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateAccActionPerformed
        // TODO add your handling code here:
        id = jTextFieldAccountID.getText();
        firstname = jTextFieldFirstname.getText();
        middlename = jTextFieldMiddlename.getText();
        lastname = jTextFieldLastname.getText();
        telephone = jTextFieldTeleNum.getText();
        username = firstname.concat("."+id);
        password = lastname.concat("123");

        if(id.equals("") || firstname.equals("") || middlename.equals("") || lastname.equals("") || telephone.equals("")){
            JOptionPane.showMessageDialog(this, "Please recheck your information"); 
        }else{
            Account u = new Account(id, firstname, middlename, lastname, telephone, username, password);
            boolean check = conn.addAccount(u);
            if(check == true){
                JOptionPane.showMessageDialog(this,"You have successfully created an account");
                String data[] = {u.getID(), u.getFirstname(), u.getMiddlename(), u.getLastname(), u.getTelephone()};
                tbl.addRow(data);
            }else{
                 JOptionPane.showMessageDialog(this, "Please recheck your information");       
            }
        }
      
    }//GEN-LAST:event_jButtonCreateAccActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        
        id = jTextFieldAccountID.getText();
        firstname = jTextFieldFirstname.getText();
        middlename = jTextFieldMiddlename.getText();
        lastname = jTextFieldLastname.getText();
        telephone = jTextFieldTeleNum.getText();
        
        Account acc = new Account(id, firstname, middlename, lastname, telephone);
        boolean check = conn.updateUser(acc);
        
        if(check == true){
            JOptionPane.showMessageDialog(this,"Updated Successfully");
            tbl.setValueAt(id, index, 0);
            tbl.setValueAt(firstname, index, 1);
            tbl.setValueAt(middlename, index, 2);
            tbl.setValueAt(lastname, index, 3);
            tbl.setValueAt(telephone, index, 4);
        }
        
        
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        String userID = jTable1.getValueAt(index, 0).toString();

        boolean check = conn.deleteUser(userID);
        if(check == true){
            JOptionPane.showMessageDialog(this,"You have successfully deleted an account");
            tbl.removeRow(index);
        } 
    }//GEN-LAST:event_jButtonDeleteActionPerformed


    private void jBtnBorrowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBorrowActionPerformed
        // TODO add your handling code here:
        Book currentBk = books.get(index);
        if(conn.availableBook(currentBk) == false){
            conn.addBorrowBook(currentBk, acc, jDate.getText());
            JOptionPane.showMessageDialog(this, "You borrowed "+currentBk.getTitle());
        }else{
            JOptionPane.showMessageDialog(this, currentBk.getTitle()+" is unavailable at the moment.");
        }
        
    }//GEN-LAST:event_jBtnBorrowActionPerformed

